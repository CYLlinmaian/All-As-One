print("{:>15s}:{:<8.2f}".format("Height",87.654321))
#{:>15s}表示右对齐15个字符,左侧空白默认用空格填充
# {:<8.2f}表示左对齐加小数点一共8位的浮点型小数,保留小数点后两位
# {:>15s}:{:<8.2f}中间的冒号,就是分隔两个数据的分隔符